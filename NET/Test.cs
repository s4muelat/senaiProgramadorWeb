int a = 10;
int b = 3;

Console.WriteLine("Soma: " + (a + b));     // 10 + 3 = 13
Console.WriteLine("Subtração: " + (a - b)); // 10 - 3 = 7
Console.WriteLine("Multiplicação: " + (a * b)); // 10 * 3 = 30
Console.WriteLine("Divisão: " + (a / b));   // 10 / 3 = 3
Console.WriteLine("Módulo: " + (a % b));    // 10 % 3 = 1